public class temp { // It's not mandatory or compulsory to have a class name starts with Capital Letter

	public static void main(String[] args) {
		
		int ok = 10;
		switch(ok) {
		
		default:
			System.out.println("default");
		case 0:
			System.out.println("true");
		case 1:
			System.out.println("false");
		}
	}
}


